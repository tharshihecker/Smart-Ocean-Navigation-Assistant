@router.get("/hazard-analysis/{latitude}/{longitude}")
async def get_hazard_analysis(
    latitude: float,
    longitude: float,
    db: Session = Depends(get_db)
):
    """Get comprehensive hazard analysis for a location"""
    try:
        # Get current weather and forecast
        current_weather = await weather_service.get_current_weather(latitude, longitude)
        forecast = await weather_service.get_forecast(latitude, longitude, 7)
        
        # Get IR content
        ir_content = await weather_service.fetch_ir_content(latitude, longitude)
        
        # Generate AI analysis using multi-agent system
        analysis = await multi_agent_service.analyze_hazards({
            "current_weather": current_weather,
            "forecast": forecast,
            "ir_content": ir_content,
            "location": {"latitude": latitude, "longitude": longitude}
        })
        
        return {
            "location": {"latitude": latitude, "longitude": longitude},
            "current_weather": current_weather,
            "forecast": forecast,
            "ir_content": ir_content,
            "hazard_analysis": analysis
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Error analyzing hazards: {str(e)}")

